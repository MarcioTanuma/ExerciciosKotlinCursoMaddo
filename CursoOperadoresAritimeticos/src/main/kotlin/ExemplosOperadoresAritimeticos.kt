fun main(){

    println("*** Exemplos de Operadores Aritiméticos ***")
    var valorA = 10.0
    var valorB = 3.0

    var soma = valorA.plus(valorB)
    var subtracao = valorA.minus(valorB)
    var multiplicacao = valorA.times(valorB)
    var divisao = valorA.div(valorB)
    var modulo = valorA.mod(valorB)


    println("**** Dados ****")
    println("Valor A       = $valorA")
    println("Valor B       = $valorB")

    println("**** Resultados ****")
    println("Soma          = $soma")
    println("Subtração     = $subtracao")
    println("Multiplicação = $multiplicacao")
    println("Divisão       = $divisao")
    println("Módulo        = $modulo")

}